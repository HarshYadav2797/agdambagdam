package encapsulation;

public class Chocolate {

private	int price=10;
private	String brand="Dairy Milk";
	
//GETTERS 	
public int getPrice()
{
	return price;
}

public String getBrand()
{
	return brand;
}


//SETTERS
public void setPrice(int a)
{
	this.price=a;
}

public void setBrand(String b)
{
	this.brand=b;
}
}




