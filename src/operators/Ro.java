package operators;

public class Ro {
	/**
	 * relational operator are used for comparison
	 * and its return type is boolean
	 * @param args
	 */
	public static void main(String[] args) {
		System.out.println("main starts");
		int num1=5; int num2=10;
		System.out.println(num1== num2);
		System.out.println(num1!=num2);
		System.out.println(num1>num2);
		System.out.println(num1<num2);
		System.out.println(num1>=num2);
		System.out.println(num1<=num2);
	}

}
